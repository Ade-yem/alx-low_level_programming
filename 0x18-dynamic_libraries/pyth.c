/**
 * add - add two integers
 * @a: integer
 * @b: integer
 * Return: addition
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two integers
 * @a: integer
 * @b: integer
 * Return: subtraction
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two integers
 * @a: integer
 * @b: integer
 * Return: multiplication
 */

int mul(int a, int b)
{
        return (a * b);
}

/**
 * div - divide two integers
 * @a: integer
 * @b: integer
 * Return: division
 */

int div(int a, int b)
{
        return (a / b);
}

/**
 * mod - mod two integers
 * @a: integer
 * @b: integer
 * Return: modulo
 */

int mod(int a, int b)
{
        return (a % b);
}
